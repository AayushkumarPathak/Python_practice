#Modify strings in python using set of in built functions

#uppercase
x="Hi everyone"
print(x.upper())
print(x.lower()) #lowercase

#remove white space
print(x.strip())

#replace
y="Hello World"
print(y.replace('H','A'))

#splitting the string
a='Hello World '
print(a.split())
print(a[0]) #---->

#string concatenation
x = 'hello'
y = 'world'
z=x+" "+y
print(z)
